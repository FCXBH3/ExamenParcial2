package Clases;
public class Persona {
    private String nombre;
    private String gmail;

    public Persona(String nombre, String gmail) {
        this.nombre = nombre;
        this.gmail = gmail;
    }

    public Persona() {
    }

    public String getNombre() {
        return nombre;
    }

    public String getGmail() {
        return gmail;
    }
    
    
    
}
